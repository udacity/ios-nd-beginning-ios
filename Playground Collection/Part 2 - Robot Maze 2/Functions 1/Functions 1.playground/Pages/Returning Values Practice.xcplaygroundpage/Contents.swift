//: ### Exercise 1
//: Define a function called `areaOfTriangle` that takes 2 `Double` parameters: `base` and `height`. The function should **return a Double** that is the area of a triangle which is `0.5 * base * height`.
//: **The solution is available on the next page!**
func areaOfTriangle(base: Double, height: Double) -> Double {
    return 0.5 * base * height
}

print(areaOfTriangle(base: 5, height: 3))
